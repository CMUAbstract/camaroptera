offset = 25

def scale(x, qual):
	sf = 2 - (qual/50)
	y = (x*sf) + 0.5
	if y < 1: 
		y = 1
	elif y > 255:
		y = 255
	return y

def quality_scaling(qual):
	scaled =  [ int(round(scale(x, qual))) for x in OG_TABLE ]
	return scaled

def main(args):
	
	scaled = quality_scaling(args.qual) 
	
	modified_header = OG_HEADER
	
	for i in range(len(scaled)):
		modified_header[offset+i] = scaled[ZIGZAG[i]] 
		
	fp = open('jpeg_header_QF%s.txt' % args.qual, 'w')

	for i in range(len(modified_header)):
		fp.write('%s' % modified_header[i])
		if i != len(modified_header) - 1:
			fp.write(' ')

	fp.close()

if __name__ == '__main__':
	parser = argparse.ArgumentParser(formatter_class=argparse.ArgumentDefaultsHelpFormatter)

	parser.add_argument(
		'--qual',
		type=int,
		default=50,
		help='JPEG Quality Factor')
	args = parser.parse_args()
	main(args)

